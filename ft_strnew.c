#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*s;
	size_t	i;

	if (!(s = (char *)malloc(size + 1)))
		return (0);
	i = 0;
	while (i < size + 1)
		s[i++] = 0;
	return (s);
}
